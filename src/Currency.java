public class Currency {
    private String name; // tên tiền tệ (vd: Dollars, Euro)

    public Currency(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    @Override
    public String toString(){
        return " "+this.name;
    } 
}
